async def import_watchlist():
    config = settings.load_config()
    engine = await get_db_engine(config.database_url)
    
    # Ensure tables exist (including the new Watchlist table)
    await create_db_and_tables(engine)
    
    session_factory = get_session_factory(engine)
    
    tickers = [t.strip() for t in HEALTHCARE_TICKERS.split("\n") if t.strip()]
    logger.info(f"Importing {len(tickers)} tickers to watchlist...")
    
    async with session_factory() as session:
        # Check existing to avoid duplicates
        existing_result = await session.execute(select(Watchlist.ticker))
        existing_tickers = set(existing_result.scalars().all())
        
        count = 0
        for ticker in tickers:
            if ticker not in existing_tickers:
                session.add(Watchlist(ticker=ticker))
                count += 1
                
        await session.commit()
        logger.success(f"Successfully added {count} new tickers to watchlist.")

if __name__ == "__main__":
    asyncio.run(import_watchlist())
